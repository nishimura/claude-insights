INTENT_CLIP = 140
IDLE_GAP_SECONDS = 30 * 60
# Empirical thresholds calibrated to the Read tool's 25000-token cap. Packets
# with significant non-ASCII (multibyte) text use roughly 2 bytes per token; a
# packet at or above ~40KB / ~700 lines can truncate on a full Read and must be
# opened with offset/limit or grep. Pure-ASCII packets fit more text per token,
# so these limits are conservative for non-ASCII-heavy transcripts.
LARGE_PACKET_BYTES = 40 * 1024
LARGE_PACKET_LINES = 700
VERY_LARGE_PACKET_BYTES = 256 * 1024
USER_CORRECTION_RE = re.compile(
    r"(wrong|not right|try again|not what i meant|instead|again)",
    re.IGNORECASE,
)
FAILURE_RE = re.compile(r"(fail|failed|failure|error|exit code [1-9]|not ok|fatal|exception)", re.IGNORECASE)
SUCCESS_RE = re.compile(r"(success|successful|passed|passing|ok|no errors|0 failures|0 errors)", re.IGNORECASE)
UUID_RE = re.compile(r"^[0-9a-f]{8}(?:-[0-9a-f]{4}){3}-[0-9a-f]{12}$", re.IGNORECASE)
UUID_PREFIX_RE = re.compile(r"^[0-9a-f]{8}(?:-[0-9a-f]{4}){0,3}(?:-[0-9a-f]{0,12})?$", re.IGNORECASE)
LATEST_RE = re.compile(r"^latest:(\d+)$", re.IGNORECASE)


def expand_home(path):
    return os.path.expanduser(path)


def compile_targets(patterns):
    compiled_patterns = []
    dirs = []
    for pattern in patterns:
        pattern = expand_home(pattern)
        compiled_patterns.append(pattern)
        for match in glob.glob(pattern):
            if os.path.isdir(match):
                dirs.append(os.path.realpath(match))
    seen = set()
    unique_dirs = []
    for path in dirs:
        path = path.rstrip("/")
        if path not in seen:
            seen.add(path)
            unique_dirs.append(path)
    return {"patterns": compiled_patterns, "dirs": unique_dirs}


def cwd_matches(cwd, targets):
    cwd = cwd.rstrip("/")
    for directory in targets["dirs"]:
        if cwd == directory or cwd.startswith(directory + "/"):
            return True
    for pattern in targets["patterns"]:
        if fnmatch.fnmatchcase(cwd, pattern) or fnmatch.fnmatch(cwd, pattern):
            return True
    return False


def clip_text(text, length):
    return " ".join(text.strip().split())[:length]


def md_cell(text):
    return text.replace("\r\n", " ").replace("\r", " ").replace("\n", " ").replace("|", "\\|")


def is_synthetic_user_text(text):
    trimmed = text.strip()
    return (
        trimmed.startswith("<local-command-caveat>")
        or trimmed.startswith("<local-command-")
        or trimmed.startswith("Caveat: The messages below were generated by the user while running local commands.")
        or re.search(r"<command-name>/(clear|exit)</command-name>", trimmed) is not None
    )


def extract_xml_tag(text, tag):
    match = re.search(r"<%s>(.*?)</%s>" % (tag, tag), text, re.DOTALL)
    if not match:
        return ""
    value = " ".join(match.group(1).strip().split())
    if html_unescape is not None:
        value = html_unescape(value)
    return value


def format_command_intent(text):
    trimmed = text.strip()
    command_name = extract_xml_tag(trimmed, "command-name")
    if not command_name:
        return ""
    if command_name in ("/clear", "/exit"):
        return ""

    command_args = extract_xml_tag(trimmed, "command-args")
    command_message = extract_xml_tag(trimmed, "command-message")
    parts = [command_name]
    if command_args:
        parts.append(command_args)
    elif command_message and command_message != command_name.lstrip("/"):
        parts.append(command_message)
    return clip_text(" ".join(parts), INTENT_CLIP)


def format_intent_text(text):
    command_intent = format_command_intent(text)
    if command_intent:
        return command_intent
    return clip_text(text, INTENT_CLIP)


def compact_path(path):
    parts = [part for part in path.split("/") if part]
    if len(parts) <= 3:
        return path
    return ".../" + "/".join(parts[-3:])


def read_jsonl_lenient(path):
    entries = []
    try:
        with open(path, "r", encoding="utf-8") as fh:
            for line in fh:
                line = line.strip()
                if not line:
                    continue
                try:
                    entry = json.loads(line)
                except Exception:
                    continue
                if isinstance(entry, dict):
                    entries.append(entry)
    except IOError:
        return []
    return entries


def first_user_text_from_entries(entries):
    for entry in entries:
        if entry.get("type") != "user":
            continue
        content = entry.get("message", {}).get("content")
        if isinstance(content, str) and content.strip():
            if is_synthetic_user_text(content):
                continue
            return format_intent_text(content)
        if isinstance(content, list):
            for block in content:
                if isinstance(block, dict) and block.get("type") == "text":
                    text = str(block.get("text", ""))
                    if text.strip():
                        if is_synthetic_user_text(text):
                            continue
                        return format_intent_text(text)
    return ""


VERIFY_RE = re.compile(
    r"\b(test|tests|phpunit|phpstan|pytest|cargo\s+test|npm\s+(run\s+)?test|npm\s+run\s+(check|lint|build)|pnpm\s+(test|lint|build|check)|yarn\s+(test|lint|build|check)|make\s+(test|check|lint)|tsc|eslint|ruff|mypy|go\s+test|bundle\s+exec\s+rspec)\b",
    re.IGNORECASE,
)


def is_verification_command(command):
    return VERIFY_RE.search(command) is not None


def timestamp_to_epoch(timestamp):
    if not timestamp:
        return None
    value = str(timestamp)
    if value.endswith("Z"):
        value = value[:-1]
    if "." in value:
        value = value.split(".", 1)[0]
    for suffix in ("+00:00", "-00:00"):
        if value.endswith(suffix):
            value = value[:-len(suffix)]
    try:
        return int((datetime.strptime(value, "%Y-%m-%dT%H:%M:%S") - datetime(1970, 1, 1)).total_seconds())
    except Exception:
        return None


def duration_metrics(entries):
    timestamps = []
    for entry in entries:
        epoch = timestamp_to_epoch(entry.get("timestamp"))
        if epoch is not None:
            timestamps.append(epoch)
    if not timestamps:
        return {"wall_duration_minutes": 0, "active_duration_minutes": 0, "large_idle_gap_count": 0}

    timestamps.sort()
    wall = max(0, int(round((timestamps[-1] - timestamps[0]) / 60.0)))
    active_seconds = 0
    large_gaps = 0
    for idx in range(1, len(timestamps)):
        gap = timestamps[idx] - timestamps[idx - 1]
        if gap > IDLE_GAP_SECONDS:
            large_gaps += 1
        else:
            active_seconds += max(0, gap)
    return {
        "wall_duration_minutes": wall,
        "active_duration_minutes": int(round(active_seconds / 60.0)),
        "large_idle_gap_count": large_gaps,
    }


def text_from_content(content):
    if isinstance(content, str):
        return content
    if isinstance(content, list):
        parts = []
        for block in content:
            if isinstance(block, dict):
                if block.get("type") == "text":
                    parts.append(str(block.get("text", "")))
                elif block.get("type") == "tool_result":
                    parts.append(str(block.get("content", "")))
        return "\n".join(parts)
    return ""


def classify_verification_result(text, is_error):
    if is_error:
        return "failure"
    if SUCCESS_RE.search(text):
        return "success"
    if FAILURE_RE.search(text):
        return "failure"
    return "unknown"


def count_user_corrections(entries):
    count = 0
    for entry in entries:
        if entry.get("type") != "user":
            continue
        content = entry.get("message", {}).get("content")
        texts = []
        if isinstance(content, str):
            texts.append(content)
        elif isinstance(content, list):
            for block in content:
                if isinstance(block, dict) and block.get("type") == "text":
                    texts.append(str(block.get("text", "")))
        for text in texts:
            if text and not is_synthetic_user_text(text) and USER_CORRECTION_RE.search(text):
                count += 1
                break
    return count


def role_label_from_agent_input(input_data):
    for key in ("subagent_type", "name", "description"):
        value = input_data.get(key)
        if isinstance(value, str) and value.strip():
            return clip_text(value, 80)
    return ""


def discover_subagent_files(parent_file, session_id):
    root = os.path.join(os.path.dirname(parent_file), session_id, "subagents")
    if not os.path.isdir(root):
        return []
    files = []
    for dirpath, _dirnames, filenames in os.walk(root):
        for filename in filenames:
            if filename.endswith(".jsonl"):
                files.append(os.path.join(dirpath, filename))
    files.sort()
    return files


def find_parent_session_files(claude_home):
    projects_dir = os.path.join(expand_home(claude_home), "projects")
    if not os.path.isdir(projects_dir):
        return []
    files = []
    sep_subagents = os.sep + "subagents" + os.sep
    for dirpath, _dirnames, filenames in os.walk(projects_dir):
        if sep_subagents in dirpath + os.sep:
            continue
        for filename in filenames:
            if filename.endswith(".jsonl"):
                files.append(os.path.join(dirpath, filename))
    files.sort(key=lambda path: os.path.getmtime(path), reverse=True)
    return files


def message_has_user_text(content):
    if isinstance(content, str):
        return bool(content.strip())
    if isinstance(content, list):
        for block in content:
            if isinstance(block, dict) and block.get("type") == "text" and str(block.get("text", "")).strip():
                return True
    return False


def session_id_matches(value, wanted):
    if not wanted:
        return True
    value = str(value or "")
    wanted = str(wanted)
    return value == wanted or value.startswith(wanted)


def inspect_session(path, targets, session_id_filter=None):
    session_id = os.path.splitext(os.path.basename(path))[0]
    matched_cwd = None
    first_ts = None
    last_ts = None
    human_users = 0
    assistant_messages = 0
    agent_calls = 0
    tools = {}

    for entry in read_jsonl_lenient(path):
        if isinstance(entry.get("sessionId"), str):
            session_id = entry.get("sessionId")
        if isinstance(entry.get("timestamp"), str):
            if first_ts is None:
                first_ts = entry.get("timestamp")
            last_ts = entry.get("timestamp")
        if isinstance(entry.get("cwd"), str):
            if targets is None:
                if matched_cwd is None:
                    matched_cwd = entry.get("cwd")
            elif cwd_matches(entry.get("cwd"), targets):
                if matched_cwd is None:
                    matched_cwd = entry.get("cwd")

        msg_type = entry.get("type")
        content = entry.get("message", {}).get("content")
        if msg_type == "user":
            if message_has_user_text(content):
                human_users += 1
        elif msg_type == "assistant":
            assistant_messages += 1
            if isinstance(content, list):
                for block in content:
                    if not isinstance(block, dict) or block.get("type") != "tool_use":
                        continue
                    name = str(block.get("name", "unknown"))
                    tools[name] = tools.get(name, 0) + 1
                    if name in ("Agent", "Task"):
                        agent_calls += 1

    if session_id_filter and not session_id_matches(session_id, session_id_filter):
        return None
    if matched_cwd is None:
        return None

    return {
        "file": path,
        "session_id": session_id,
        "matched_cwd": matched_cwd,
        "first_ts": first_ts or "",
        "last_ts": last_ts or "",
        "human_users": human_users,
        "assistant_messages": assistant_messages,
        "agent_calls": agent_calls,
        "tools": sort_counts(tools),
        "mtime": os.path.getmtime(path),
    }


def sort_counts(counts):
    return dict(sorted(counts.items(), key=lambda item: (-item[1], item[0])))


def format_counts(counts):
    if not counts:
        return "none"
    return ", ".join("%s %s" % (name, count) for name, count in counts.items())


def packet_read_strategy(size_bytes, line_count):
    if size_bytes >= VERY_LARGE_PACKET_BYTES:
        return "very-large: do not full-read; use targeted offset/limit ranges or grep only"
    if size_bytes >= LARGE_PACKET_BYTES or line_count >= LARGE_PACKET_LINES:
        return "large: full read may exceed the Read 25000-token cap; use offset/limit or grep"
    return "normal: full read should fit within the Read 25000-token cap"


def interval_union_minutes(intervals):
    cleaned = []
    for start, end in intervals:
        if start is None or end is None:
            continue
        if end < start:
            start, end = end, start
        cleaned.append((start, end))
    if not cleaned:
        return 0
    cleaned.sort()
    total = 0
    current_start, current_end = cleaned[0]
    for start, end in cleaned[1:]:
        if start <= current_end:
            current_end = max(current_end, end)
        else:
            total += current_end - current_start
            current_start, current_end = start, end
    total += current_end - current_start
    return int(round(total / 60.0))


def transcript_interval(entries):
    timestamps = []
    for entry in entries:
        epoch = timestamp_to_epoch(entry.get("timestamp"))
        if epoch is not None:
            timestamps.append(epoch)
    if not timestamps:
        return (None, None)
    return (min(timestamps), max(timestamps))


def transcript_active_intervals(entries):
    timestamps = []
    for entry in entries:
        epoch = timestamp_to_epoch(entry.get("timestamp"))
        if epoch is not None:
            timestamps.append(epoch)
    timestamps = sorted(set(timestamps))
    intervals = []
    for idx in range(1, len(timestamps)):
        start = timestamps[idx - 1]
        end = timestamps[idx]
        if end >= start and end - start <= IDLE_GAP_SECONDS:
            intervals.append((start, end))
    return intervals


def summarize_session(meta):
    entries = read_jsonl_lenient(meta["file"])
    tools = {}
    files = {}
    errors = 0
    interrupted = False
    agent_calls = 0
    logical_roles = {}
    edit_write_count = 0
    verification_count = 0
    verification_success_count = 0
    verification_failure_count = 0
    verification_unknown_count = 0
    pending_tools = {}

    for entry in entries:
        content = entry.get("message", {}).get("content")
        if entry.get("type") == "assistant" and isinstance(content, list):
            for block in content:
                if not isinstance(block, dict) or block.get("type") != "tool_use":
                    continue
                name = str(block.get("name", "unknown"))
                tool_id = block.get("id")
                tools[name] = tools.get(name, 0) + 1
                if name in ("Edit", "MultiEdit", "Write", "NotebookEdit"):
                    edit_write_count += 1
                input_data = block.get("input")
                if isinstance(input_data, dict):
                    for key in ("file_path", "path"):
                        value = input_data.get(key)
                        if isinstance(value, str) and value:
                            files[value] = files.get(value, 0) + 1
                    command = input_data.get("command")
                    if isinstance(tool_id, str):
                        pending_tools[tool_id] = {
                            "name": name,
                            "command": command if isinstance(command, str) else "",
                        }
                    if isinstance(command, str) and is_verification_command(command):
                        verification_count += 1
                    if name in ("Agent", "Task"):
                        agent_calls += 1
                        role = role_label_from_agent_input(input_data)
                        if role:
                            logical_roles[role] = True

        if entry.get("type") == "user" and isinstance(content, list):
            for block in content:
                if not isinstance(block, dict):
                    continue
                if block.get("type") == "tool_result" and block.get("is_error") is True:
                    errors += 1
                if block.get("type") == "tool_result":
                    tool_id = block.get("tool_use_id")
                    tool_meta = pending_tools.get(tool_id) if isinstance(tool_id, str) else None
                    if tool_meta and is_verification_command(tool_meta.get("command", "")):
                        result = classify_verification_result(str(block.get("content", "")), block.get("is_error") is True)
                        if result == "success":
                            verification_success_count += 1
                        elif result == "failure":
                            verification_failure_count += 1
                        else:
                            verification_unknown_count += 1
                if block.get("type") == "text" and "[Request interrupted by user" in str(block.get("text", "")):
                    interrupted = True
        elif entry.get("type") == "user" and isinstance(content, str):
            if "[Request interrupted by user" in content:
                interrupted = True

    tools = sort_counts(tools)
    files = sort_counts(files)
    raw_subagent_count = len(discover_subagent_files(meta["file"], meta["session_id"]))
    logical_role_count = len(logical_roles)
    tool_count = sum(tools.values())
    first_intent = first_user_text_from_entries(entries)
    user_correction_count = count_user_corrections(entries)
    durations = duration_metrics(entries)

    if tool_count == 0 and int(meta["assistant_messages"]) == 0:
        signal_class = "no-op"
    elif tool_count == 0 and int(meta["human_users"]) <= 1 and int(meta["assistant_messages"]) <= 1:
        signal_class = "no-op"
    elif (
        edit_write_count == 0
        and verification_count == 0
        and agent_calls == 0
        and raw_subagent_count == 0
        and int(meta["human_users"]) <= 2
        and int(meta["assistant_messages"]) <= 3
    ):
        signal_class = "low-signal"
    else:
        signal_class = "substantive"

    docs_signal = any("/docs/" in path or "claude-docs" in path or path.endswith(".md") for path in files)
    intent_lower = first_intent.lower()
    debugging_signal = errors > 0 or any(word in intent_lower for word in ("error", "fail", "fix", "broken", "debug"))
    review_signal = any(word in intent_lower for word in ("review", "diff", "explain"))
    planning_signal = "plan" in intent_lower or any("PlanMode" == name or "ExitPlanMode" == name for name in tools)
    investigation_signal = edit_write_count == 0 and (tools.get("Read", 0) + tools.get("Grep", 0) + tools.get("Glob", 0) + tools.get("Bash", 0)) >= 3

    if agent_calls > 0 and raw_subagent_count > 0:
        session_kind = "delegated"
    elif agent_calls > 0:
        session_kind = "delegation-attempt"
    elif signal_class == "no-op":
        session_kind = "no-op"
    elif docs_signal and edit_write_count > 0:
        session_kind = "docs"
    elif debugging_signal:
        session_kind = "debugging"
    elif review_signal:
        session_kind = "review"
    elif planning_signal:
        session_kind = "planning"
    elif edit_write_count > 0:
        session_kind = "implementation"
    elif verification_count > 0:
        session_kind = "verification"
    elif investigation_signal:
        session_kind = "investigation"
    else:
        session_kind = "conversation"

    flags = []
    if raw_subagent_count >= 2 or logical_role_count >= 2 or agent_calls >= 2:
        flags.append("agent-heavy")
    if errors >= 2:
        flags.append("error-heavy")
    if verification_count >= 3:
        flags.append("verification-heavy")
    if edit_write_count >= 10:
        flags.append("edit-heavy")
    if interrupted:
        flags.append("interrupted")
    if signal_class == "no-op":
        flags.append("no-op")
    if docs_signal:
        flags.append("docs")
    if edit_write_count > 0:
        flags.append("implementation")
    if user_correction_count > 0:
        flags.append("user-correction")

    return {
        "first_intent": first_intent,
        "session_kind": session_kind,
        "signal_class": signal_class,
        "substantive": signal_class == "substantive",
        "edit_write_count": edit_write_count,
        "verification_count": verification_count,
        "verification_success_count": verification_success_count,
        "verification_failure_count": verification_failure_count,
        "verification_unknown_count": verification_unknown_count,
        "error_count": errors,
        "interrupted": interrupted,
        "raw_subagent_transcript_count": raw_subagent_count,
        "logical_subagent_role_count": logical_role_count,
        "logical_subagent_roles": list(logical_roles.keys()),
        "report_flags": flags,
        "user_correction_count": user_correction_count,
        "wall_duration_minutes": durations["wall_duration_minutes"],
        "active_duration_minutes": durations["active_duration_minutes"],
        "large_idle_gap_count": durations["large_idle_gap_count"],
        "top_files": dict(list(files.items())[:8]),
        "tools": tools,
        "tool_use_count": tool_count,
    }


def parent_agent_launches(parent_entries):
    launches = []
    for entry in parent_entries:
        content = entry.get("message", {}).get("content")
        if entry.get("type") != "assistant" or not isinstance(content, list):
            continue
        for block in content:
            if not isinstance(block, dict) or block.get("type") != "tool_use":
                continue
            if block.get("name") not in ("Agent", "Task"):
                continue
            input_data = block.get("input")
            if not isinstance(input_data, dict):
                continue
            label = role_label_from_agent_input(input_data)
            prompt = input_data.get("prompt")
            if label and isinstance(prompt, str) and prompt.strip():
                launches.append({
                    "label": label,
                    "prompt": " ".join(prompt.split()),
                })
    return launches


def extract_ascii_quoted_label(text):
    for value in re.findall(r"\u300c([A-Za-z][A-Za-z0-9_.-]{1,79})\u300d", text):
        lowered = value.lower()
        if lowered not in ("team", "task", "agent") and "issue" not in lowered:
            return value
    return ""


def extract_subagent_role(entries, fallback, launches):
    first_text = ""
    for entry in entries:
        if entry.get("type") != "user":
            continue
        text = text_from_content(entry.get("message", {}).get("content"))
        if not text.strip():
            continue
        first_text = " ".join(text.split())
        quoted = extract_ascii_quoted_label(text)
        if quoted:
            return clip_text(quoted, 80)
        for launch in launches:
            prompt = launch["prompt"]
            if prompt and (first_text.startswith(prompt[:200]) or prompt[:200] in first_text):
                return launch["label"]
        match = re.search(r"member(?:\s+name)?\s*[:=]\s*([A-Za-z0-9_.-]{1,80})", text, re.IGNORECASE)
        if match:
            return clip_text(match.group(1), 80)
        match = re.search(r"role\s*[:=]\s*([A-Za-z0-9_.-]{1,80})", text, re.IGNORECASE)
        if match:
            return clip_text(match.group(1), 80)
        match = re.search(r"<teammate-message[^>]*\bsummary=\"([^\"]{1,80})\"", text)
        if match:
            return clip_text(match.group(1), 80)
        break
    if first_text:
        best = None
        best_score = 0
        first_words = set(first_text.lower().split())
        for launch in launches:
            prompt_words = set(launch["prompt"].lower().split())
            score = len(first_words.intersection(prompt_words))
            if score > best_score:
                best = launch["label"]
                best_score = score
        if best and best_score >= 8:
            return best
    return fallback


