        aggregate_totals["no_op"] += 1 if summary["signal_class"] == "no-op" else 0
        aggregate_totals["low_signal"] += 1 if summary["signal_class"] == "low-signal" else 0
        aggregate_totals["edit_write_count"] += summary["edit_write_count"]
