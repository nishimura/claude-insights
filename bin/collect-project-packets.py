#!/usr/bin/env python3
from __future__ import print_function

import argparse
import fnmatch
import glob
import json
import os
import re
import subprocess
import sys
from datetime import datetime
try:
    from html import unescape as html_unescape
except ImportError:
    html_unescape = None

INTENT_CLIP = 140


def expand_home(path):
    return os.path.expanduser(path)


def compile_targets(patterns):
    compiled_patterns = []
    dirs = []
    for pattern in patterns:
        pattern = expand_home(pattern)
        compiled_patterns.append(pattern)
        for match in glob.glob(pattern):
            if os.path.isdir(match):
                dirs.append(os.path.realpath(match))
    seen = set()
    unique_dirs = []
    for path in dirs:
        path = path.rstrip("/")
        if path not in seen:
            seen.add(path)
            unique_dirs.append(path)
    return {"patterns": compiled_patterns, "dirs": unique_dirs}


def cwd_matches(cwd, targets):
    cwd = cwd.rstrip("/")
    for directory in targets["dirs"]:
        if cwd == directory or cwd.startswith(directory + "/"):
            return True
    for pattern in targets["patterns"]:
        if fnmatch.fnmatchcase(cwd, pattern) or fnmatch.fnmatch(cwd, pattern):
            return True
    return False


def clip_text(text, length):
    return " ".join(text.strip().split())[:length]


def md_cell(text):
    return text.replace("\r\n", " ").replace("\r", " ").replace("\n", " ").replace("|", "\\|")


def is_synthetic_user_text(text):
    trimmed = text.strip()
    return (
        trimmed.startswith("<local-command-caveat>")
        or trimmed.startswith("<local-command-")
        or trimmed.startswith("Caveat: The messages below were generated by the user while running local commands.")
        or re.search(r"<command-name>/(clear|exit)</command-name>", trimmed) is not None
    )


def extract_xml_tag(text, tag):
    match = re.search(r"<%s>(.*?)</%s>" % (tag, tag), text, re.DOTALL)
    if not match:
        return ""
    value = " ".join(match.group(1).strip().split())
    if html_unescape is not None:
        value = html_unescape(value)
    return value


def format_command_intent(text):
    trimmed = text.strip()
    command_name = extract_xml_tag(trimmed, "command-name")
    if not command_name:
        return ""
    if command_name in ("/clear", "/exit"):
        return ""

    command_args = extract_xml_tag(trimmed, "command-args")
    command_message = extract_xml_tag(trimmed, "command-message")
    parts = [command_name]
    if command_args:
        parts.append(command_args)
    elif command_message and command_message != command_name.lstrip("/"):
        parts.append(command_message)
    return clip_text(" ".join(parts), INTENT_CLIP)


def format_intent_text(text):
    command_intent = format_command_intent(text)
    if command_intent:
        return command_intent
    return clip_text(text, INTENT_CLIP)


def compact_path(path):
    parts = [part for part in path.split("/") if part]
    if len(parts) <= 3:
        return path
    return ".../" + "/".join(parts[-3:])


def read_jsonl_lenient(path):
    entries = []
    try:
        with open(path, "r", encoding="utf-8") as fh:
            for line in fh:
                line = line.strip()
                if not line:
                    continue
                try:
                    entry = json.loads(line)
                except Exception:
                    continue
                if isinstance(entry, dict):
                    entries.append(entry)
    except IOError:
        return []
    return entries


def first_user_text_from_entries(entries):
    for entry in entries:
        if entry.get("type") != "user":
            continue
        content = entry.get("message", {}).get("content")
        if isinstance(content, str) and content.strip():
            if is_synthetic_user_text(content):
                continue
            return format_intent_text(content)
        if isinstance(content, list):
            for block in content:
                if isinstance(block, dict) and block.get("type") == "text":
                    text = str(block.get("text", ""))
                    if text.strip():
                        if is_synthetic_user_text(text):
                            continue
                        return format_intent_text(text)
    return ""


VERIFY_RE = re.compile(
    r"\b(test|tests|phpunit|phpstan|pytest|cargo\s+test|npm\s+(run\s+)?test|npm\s+run\s+(check|lint|build)|pnpm\s+(test|lint|build|check)|yarn\s+(test|lint|build|check)|make\s+(test|check|lint)|tsc|eslint|ruff|mypy|go\s+test|bundle\s+exec\s+rspec)\b",
    re.IGNORECASE,
)


def is_verification_command(command):
    return VERIFY_RE.search(command) is not None


def role_label_from_agent_input(input_data):
    for key in ("subagent_type", "name", "description"):
        value = input_data.get(key)
        if isinstance(value, str) and value.strip():
            return clip_text(value, 80)
    return ""


def discover_subagent_files(parent_file, session_id):
    root = os.path.join(os.path.dirname(parent_file), session_id, "subagents")
    if not os.path.isdir(root):
        return []
    files = []
    for dirpath, _dirnames, filenames in os.walk(root):
        for filename in filenames:
            if filename.endswith(".jsonl"):
                files.append(os.path.join(dirpath, filename))
    files.sort()
    return files


def find_parent_session_files(claude_home):
    projects_dir = os.path.join(expand_home(claude_home), "projects")
    if not os.path.isdir(projects_dir):
        return []
    files = []
    sep_subagents = os.sep + "subagents" + os.sep
    for dirpath, _dirnames, filenames in os.walk(projects_dir):
        if sep_subagents in dirpath + os.sep:
            continue
        for filename in filenames:
            if filename.endswith(".jsonl"):
                files.append(os.path.join(dirpath, filename))
    files.sort(key=lambda path: os.path.getmtime(path), reverse=True)
    return files


def message_has_user_text(content):
    if isinstance(content, str):
        return bool(content.strip())
    if isinstance(content, list):
        for block in content:
            if isinstance(block, dict) and block.get("type") == "text" and str(block.get("text", "")).strip():
                return True
    return False


def inspect_session(path, targets):
    session_id = os.path.splitext(os.path.basename(path))[0]
    matched_cwd = None
    first_ts = None
    last_ts = None
    human_users = 0
    assistant_messages = 0
    agent_calls = 0
    tools = {}

    for entry in read_jsonl_lenient(path):
        if isinstance(entry.get("sessionId"), str):
            session_id = entry.get("sessionId")
        if isinstance(entry.get("timestamp"), str):
            if first_ts is None:
                first_ts = entry.get("timestamp")
            last_ts = entry.get("timestamp")
        if isinstance(entry.get("cwd"), str) and cwd_matches(entry.get("cwd"), targets):
            if matched_cwd is None:
                matched_cwd = entry.get("cwd")

        msg_type = entry.get("type")
        content = entry.get("message", {}).get("content")
        if msg_type == "user":
            if message_has_user_text(content):
                human_users += 1
        elif msg_type == "assistant":
            assistant_messages += 1
            if isinstance(content, list):
                for block in content:
                    if not isinstance(block, dict) or block.get("type") != "tool_use":
                        continue
                    name = str(block.get("name", "unknown"))
                    tools[name] = tools.get(name, 0) + 1
                    if name in ("Agent", "Task"):
                        agent_calls += 1

    if matched_cwd is None:
        return None

    return {
        "file": path,
        "session_id": session_id,
        "matched_cwd": matched_cwd,
        "first_ts": first_ts or "",
        "last_ts": last_ts or "",
        "human_users": human_users,
        "assistant_messages": assistant_messages,
        "agent_calls": agent_calls,
        "tools": sort_counts(tools),
        "mtime": os.path.getmtime(path),
    }


def sort_counts(counts):
    return dict(sorted(counts.items(), key=lambda item: (-item[1], item[0])))


def format_counts(counts):
    if not counts:
        return "none"
    return ", ".join("%s %s" % (name, count) for name, count in counts.items())


def summarize_session(meta):
    entries = read_jsonl_lenient(meta["file"])
    tools = {}
    files = {}
    errors = 0
    interrupted = False
    agent_calls = 0
    logical_roles = {}
    edit_write_count = 0
    verification_count = 0

    for entry in entries:
        content = entry.get("message", {}).get("content")
        if entry.get("type") == "assistant" and isinstance(content, list):
            for block in content:
                if not isinstance(block, dict) or block.get("type") != "tool_use":
                    continue
                name = str(block.get("name", "unknown"))
                tools[name] = tools.get(name, 0) + 1
                if name in ("Edit", "MultiEdit", "Write", "NotebookEdit"):
                    edit_write_count += 1
                input_data = block.get("input")
                if isinstance(input_data, dict):
                    for key in ("file_path", "path"):
                        value = input_data.get(key)
                        if isinstance(value, str) and value:
                            files[value] = files.get(value, 0) + 1
                    command = input_data.get("command")
                    if isinstance(command, str) and is_verification_command(command):
                        verification_count += 1
                    if name in ("Agent", "Task"):
                        agent_calls += 1
                        role = role_label_from_agent_input(input_data)
                        if role:
                            logical_roles[role] = True

        if entry.get("type") == "user" and isinstance(content, list):
            for block in content:
                if not isinstance(block, dict):
                    continue
                if block.get("type") == "tool_result" and block.get("is_error") is True:
                    errors += 1
                if block.get("type") == "text" and "[Request interrupted by user" in str(block.get("text", "")):
                    interrupted = True
        elif entry.get("type") == "user" and isinstance(content, str):
            if "[Request interrupted by user" in content:
                interrupted = True

    tools = sort_counts(tools)
    files = sort_counts(files)
    raw_subagent_count = len(discover_subagent_files(meta["file"], meta["session_id"]))
    logical_role_count = len(logical_roles)
    tool_count = sum(tools.values())

    if tool_count == 0 and int(meta["assistant_messages"]) == 0:
        signal_class = "no-op"
    elif tool_count == 0 and int(meta["human_users"]) <= 1 and int(meta["assistant_messages"]) <= 1:
        signal_class = "no-op"
    elif (
        edit_write_count == 0
        and verification_count == 0
        and agent_calls == 0
        and raw_subagent_count == 0
        and int(meta["human_users"]) <= 2
        and int(meta["assistant_messages"]) <= 3
    ):
        signal_class = "low-signal"
    else:
        signal_class = "substantive"

    if agent_calls > 0 and raw_subagent_count > 0:
        session_kind = "delegated"
    elif agent_calls > 0:
        session_kind = "delegation-attempt"
    elif edit_write_count > 0:
        session_kind = "implementation"
    elif verification_count > 0:
        session_kind = "verification"
    elif signal_class == "no-op":
        session_kind = "no-op"
    else:
        session_kind = "conversation"

    return {
        "first_intent": first_user_text_from_entries(entries),
        "session_kind": session_kind,
        "signal_class": signal_class,
        "substantive": signal_class == "substantive",
        "edit_write_count": edit_write_count,
        "verification_count": verification_count,
        "error_count": errors,
        "interrupted": interrupted,
        "raw_subagent_transcript_count": raw_subagent_count,
        "logical_subagent_role_count": logical_role_count,
        "logical_subagent_roles": list(logical_roles.keys()),
        "top_files": dict(list(files.items())[:8]),
        "tools": tools,
    }


def run_packet_builder(session_file):
    builder = os.path.join(os.path.dirname(__file__), "build-session-packet.py")
    cmd = [sys.executable, builder, session_file]
    output = subprocess.check_output(cmd)
    if not output:
        raise RuntimeError("packet builder produced no output for %s" % session_file)
    return output.decode("utf-8")


def ensure_dir(path):
    if not os.path.isdir(path):
        os.makedirs(path)


def write_text(path, text):
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(text)


def write_json(path, data):
    with open(path, "w", encoding="utf-8") as fh:
        json.dump(data, fh, ensure_ascii=False, indent=4)
        fh.write("\n")


def parse_args(argv):
    parser = argparse.ArgumentParser(
        description="Collect Claude Code session packets for cwd-scoped insights."
    )
    repo_root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    default_home = os.path.join(os.environ.get("HOME", ""), ".claude") if os.environ.get("HOME") else ".claude"
    parser.add_argument("--out", default=None, help="Output run directory. Default: data/runs/<timestamp>")
    parser.add_argument("--limit", type=int, default=30, help="Maximum matching sessions to packetize. Default: 30")
    parser.add_argument("--claude-home", default=default_home, help="Claude config dir. Default: ~/.claude")
    parser.add_argument("patterns", nargs="+", help="cwd patterns")
    args = parser.parse_args(argv[1:])
    if args.limit < 1:
        args.limit = 1
    if args.out is None:
        args.out = os.path.join(repo_root, "data", "runs", datetime.utcnow().strftime("%Y%m%d_%H%M%S"))
    return args


def main(argv):
    args = parse_args(argv)
    out_dir = expand_home(args.out).rstrip("/")
    packet_dir = os.path.join(out_dir, "packets")
    ensure_dir(packet_dir)

    targets = compile_targets(args.patterns)
    matches = []
    for session_file in find_parent_session_files(args.claude_home):
        meta = inspect_session(session_file, targets)
        if meta is not None:
            matches.append(meta)
    matches.sort(key=lambda item: item.get("mtime", 0), reverse=True)
    selected = matches[: args.limit]

    index_rows = []
    next_sessions = []
    aggregate_sessions = []
    aggregate_totals = {
        "sessions": 0,
        "substantive": 0,
        "no_op": 0,
        "low_signal": 0,
        "edit_write_count": 0,
        "verification_count": 0,
        "error_count": 0,
        "interrupted": 0,
        "raw_subagent_transcript_count": 0,
        "logical_subagent_role_count": 0,
    }
    aggregate_kinds = {}
    aggregate_files = {}

    for meta in selected:
        short_id = str(meta["session_id"])[:8]
        packet_path = os.path.join(packet_dir, short_id + ".md")
        summary = summarize_session(meta)
        packet = run_packet_builder(meta["file"])
        write_text(packet_path, packet)

        index_rows.append({"short_id": short_id, "packet": packet_path, "meta": meta, "summary": summary})
        agent_calls = summary["tools"].get("Agent", 0) + summary["tools"].get("Task", 0)
        aggregate_session = {
            "session": short_id,
            "session_id": meta["session_id"],
            "packet": packet_path,
            "cwd": meta["matched_cwd"],
            "started": meta["first_ts"],
            "ended": meta["last_ts"],
            "human_users": meta["human_users"],
            "assistant_messages": meta["assistant_messages"],
            "agent_calls": agent_calls,
            "first_intent": summary["first_intent"],
            "session_kind": summary["session_kind"],
            "signal_class": summary["signal_class"],
            "substantive": summary["substantive"],
            "edit_write_count": summary["edit_write_count"],
            "verification_count": summary["verification_count"],
            "error_count": summary["error_count"],
            "interrupted": summary["interrupted"],
            "raw_subagent_transcript_count": summary["raw_subagent_transcript_count"],
            "logical_subagent_role_count": summary["logical_subagent_role_count"],
            "logical_subagent_roles": summary["logical_subagent_roles"],
            "top_files": summary["top_files"],
            "top_tools": dict(list(summary["tools"].items())[:8]),
        }
        aggregate_sessions.append(aggregate_session)

        aggregate_totals["sessions"] += 1
        aggregate_totals["substantive"] += 1 if summary["substantive"] else 0
        aggregate_totals["no_op"] += 1 if summary["signal_class"] == "no-op" else 0
        aggregate_totals["low_signal"] += 1 if summary["signal_class"] == "low-signal" else 0
        aggregate_totals["edit_write_count"] += summary["edit_write_count"]
        aggregate_totals["verification_count"] += summary["verification_count"]
        aggregate_totals["error_count"] += summary["error_count"]
        aggregate_totals["interrupted"] += 1 if summary["interrupted"] else 0
        aggregate_totals["raw_subagent_transcript_count"] += summary["raw_subagent_transcript_count"]
        aggregate_totals["logical_subagent_role_count"] += summary["logical_subagent_role_count"]
        aggregate_kinds[summary["session_kind"]] = aggregate_kinds.get(summary["session_kind"], 0) + 1
        for file_path, count in summary["top_files"].items():
            aggregate_files[file_path] = aggregate_files.get(file_path, 0) + count

        next_sessions.append({
            "session": short_id,
            "packet": packet_path,
            "cwd": meta["matched_cwd"],
            "session_kind": summary["session_kind"],
            "signal_class": summary["signal_class"],
            "first_intent": summary["first_intent"],
            "agent_calls": agent_calls,
            "human_users": meta["human_users"],
            "raw_subagent_transcript_count": summary["raw_subagent_transcript_count"],
            "logical_subagent_role_count": summary["logical_subagent_role_count"],
        })

    aggregate_kinds = sort_counts(aggregate_kinds)
    aggregate_files = sort_counts(aggregate_files)

    index = []
    index.append("# Claude Insights Run Index\n")
    index.append("Read this index first. Open individual packet files only when needed for the final report.\n")
    index.append("## Scope\n")
    index.append("- Patterns: `%s`" % "`, `".join(args.patterns))
    index.append("- Expanded directories: %s" % ("none" if len(targets["dirs"]) == 0 else len(targets["dirs"])))
    index.append("- Matching sessions found: %s" % len(matches))
    index.append("- Sessions packetized: %s" % len(selected))
    index.append("- Aggregate JSON: `%s`" % os.path.join(out_dir, "aggregate.json"))
    index.append("- Output directory: `%s`\n" % out_dir)

    index.append("## Aggregate Signals\n")
    index.append("- Session kinds: %s" % format_counts(aggregate_kinds))
    index.append("- Signal classes: substantive %s, low-signal %s, no-op %s" % (
        aggregate_totals["substantive"],
        aggregate_totals["low_signal"],
        aggregate_totals["no_op"],
    ))
    index.append("- Edit/write count: %s" % aggregate_totals["edit_write_count"])
    index.append("- Verification count: %s" % aggregate_totals["verification_count"])
    index.append("- Error count: %s" % aggregate_totals["error_count"])
    index.append("- Interrupted sessions: %s" % aggregate_totals["interrupted"])
    index.append("- Raw subagent transcripts: %s" % aggregate_totals["raw_subagent_transcript_count"])
    index.append("- Logical subagent roles: %s" % aggregate_totals["logical_subagent_role_count"])
    index.append("- Top files: %s\n" % format_counts(dict(list(aggregate_files.items())[:8])))

    index.append("## Packetized Sessions\n")
    if not index_rows:
        index.append("_No matching sessions found._")
    else:
        index.append("| Session | Started | Kind | Signal | First intent | Edit/write | Verify | Errors | Interrupted | Raw subagents | Logical roles | Top files | Packet |")
        index.append("|---|---:|---|---|---|---:|---:|---:|---|---:|---:|---|---|")
        for row in index_rows:
            meta = row["meta"]
            summary = row["summary"]
            top_files = []
            for file_path, count in list(summary["top_files"].items())[:3]:
                top_files.append("%s %s" % (compact_path(file_path), count))
            files = md_cell("none" if not top_files else ", ".join(top_files))
            interrupted = "yes" if summary["interrupted"] else "no"
            index.append("| `%s` | `%s` | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s | `%s` |" % (
                row["short_id"],
                meta["first_ts"],
                summary["session_kind"],
                summary["signal_class"],
                md_cell(summary["first_intent"]),
                summary["edit_write_count"],
                summary["verification_count"],
                summary["error_count"],
                interrupted,
                summary["raw_subagent_transcript_count"],
                summary["logical_subagent_role_count"],
                files,
                row["packet"],
            ))

    index.append("\n## Report Guidance\n")
    index.append("- Start from `aggregate.json` and this index before opening packets.")
    index.append("- Treat `raw_subagent_transcript_count` as storage/transcript count and `logical_subagent_role_count` as the distinct delegated role labels observed in parent tool calls.")
    index.append("- Prefer substantive sessions for first-pass packet reading; sample no-op or low-signal sessions only when explaining noise or interruptions.")
    index.append("- Analyze main-session coordination separately from subagent activity.")
    index.append("- Report delegation patterns, friction, useful agent workflows, and missed opportunities.")
    index.append("- Ground conclusions in the packet files listed above; note sampling limits in the final report.")

    write_text(os.path.join(out_dir, "index.md"), "\n".join(index) + "\n")

    aggregate = {
        "schema_version": 1,
        "generated_at": datetime.utcnow().replace(microsecond=0).isoformat() + "Z",
        "index": os.path.join(out_dir, "index.md"),
        "output_dir": out_dir,
        "patterns": args.patterns,
        "expanded_directory_count": len(targets["dirs"]),
        "matching_session_count": len(matches),
        "packet_count": len(selected),
        "totals": aggregate_totals,
        "session_kinds": aggregate_kinds,
        "top_files": dict(list(aggregate_files.items())[:20]),
        "sessions": aggregate_sessions,
    }
    write_json(os.path.join(out_dir, "aggregate.json"), aggregate)

    next_action = {
        "index": os.path.join(out_dir, "index.md"),
        "aggregate": os.path.join(out_dir, "aggregate.json"),
        "output_dir": out_dir,
        "packet_count": len(selected),
        "matching_session_count": len(matches),
        "patterns": args.patterns,
        "sessions": next_sessions,
        "suggested_next_step": "Read aggregate.json and index.md, then selectively read packet files to write report.md in this output directory.",
    }
    write_json(os.path.join(out_dir, "next-action.json"), next_action)

    print("Wrote insights run data:")
    print(out_dir)
    print("Index:")
    print(os.path.join(out_dir, "index.md"))
    print("Aggregate:")
    print(os.path.join(out_dir, "aggregate.json"))
    print("Next action:")
    print(os.path.join(out_dir, "next-action.json"))
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv))
